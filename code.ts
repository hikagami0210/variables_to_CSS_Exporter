async function generateAndSend(format: "standard" | "tailwind") {
  try {
    const collections =
      await figma.variables.getLocalVariableCollectionsAsync();
    const variables = await figma.variables.getLocalVariablesAsync();
    const variableMap = new Map<string, Variable>();
    variables.forEach((v) => variableMap.set(v.id, v));

    let cssOutput = `/* Generated by Figma Variables Exporter */\n`;
    if (format === "tailwind") {
      cssOutput += `/* Tailwind CSS v4 Theme Format */\n`;
      cssOutput += `@import "tailwindcss";\n\n`;
    } else {
      cssOutput += `/* Standard CSS Variables */\n\n`;
    }

    for (const collection of collections) {
      for (const mode of collection.modes) {
        const modeName = mode.name.toLowerCase();
        const isDefault =
          collection.defaultModeId === mode.modeId || modeName === "light";

        let selector =
          format === "tailwind"
            ? isDefault
              ? "@theme"
              : `[data-theme="${modeName}"]`
            : isDefault
              ? ":root"
              : `[data-theme="${modeName}"]`;

        cssOutput += `/* Collection: ${collection.name}, Mode: ${mode.name} */\n`;
        cssOutput += `${selector} {\n`;

        for (const variable of variables) {
          if (variable.variableCollectionId !== collection.id) continue;
          if (variable.hiddenFromPublishing || variable.remote) continue;

          const value = variable.valuesByMode[mode.modeId];
          if (value !== undefined) {
            const cssName =
              format === "tailwind"
                ? toTailwindName(variable.name, variable.resolvedType)
                : normalizeName(variable.name);
            const cssValue = formatValue(
              value,
              variable.resolvedType,
              variable.name,
              format,
              variableMap,
            );
            if (variable.description)
              cssOutput += `  /* ${variable.description.replace(/\n/g, " ")} */\n`;
            cssOutput += `  ${cssName}: ${cssValue};\n`;
          }
        }
        cssOutput += `}\n\n`;
      }
    }
    figma.ui.postMessage({ type: "export-css", data: cssOutput });
  } catch (error) {
    console.error("生成エラー:", error);
    figma.ui.postMessage({
      type: "export-css",
      data: "エラーが発生しました。コンソールを確認してください。",
    });
  }
}

// プラグイン起動時の初回実行
generateAndSend("tailwind");

// UIからのフォーマット変更リクエストを受信
figma.ui.onmessage = (msg) => {
  if (msg.type === "generate") {
    generateAndSend(msg.format);
  }
};
